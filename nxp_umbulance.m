% --- Executes on button press in pushbutton3.
function pushbutton3_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton3 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
global s;
global rate;
global COM;
global received;


received=[];
COM=sprintf('com%d', get(handles.popcom, 'value'));
rate = 115200;
global j;
j=1;

s=serial(COM);
set(s,'BaudRate',rate);%%%Baud初始化
set(s,'DataBits',8);%%%设置数据长度
set(s,'StopBits',1);%%%设置停止位长度
set(s,'InputBufferSize',10240000);%%%设置输入缓冲区大小为10M


%串口事件回调设置
s.BytesAvailableFcnMode='byte';
s.BytesAvailableFcnCount=100; %输入缓冲区存在字节触发回调函数
s.BytesAvailableFcn={@readData,handles};%回调函数的指定
try fopen(s);%打开串口
end

function readData(obj, event,handles)
proco=zeros(1,26);
proco=fread(obj,100,'uint8');
global j
for i=1:73
      if proco(i)==170
       if proco(i+1)==170
          acc_x=proco(i+8)*256+proco(i+9);
          if acc_x>=32768
                yaccx=-bitcmp(acc_x-1,16);
                else yaccx=acc_x;
          end
          
          acc_y=proco(i+10)*256+proco(i+11);
          if acc_y>=32768
                yaccy=-bitcmp(acc_y-1,16);
                else yaccy=acc_y;
          end
          
          acc_z=proco(i+12)*256+proco(i+13);
          if acc_z>=32768
                yaccz=-bitcmp(acc_z-1,16);
                else yaccz=acc_z;
          end
          
          gyo_x=proco(i+14)*256+proco(i+15);
          if gyo_x>=32768
                ygyox=-bitcmp(gyo_x-1,16);
                else ygyox=gyo_x;
          end
          
          gyo_y=proco(i+16)*256+proco(i+17);
          if gyo_y>=32768
                ygyoy=-bitcmp(gyo_y-1,16);
                else ygyoy=gyo_y;
          end
          
          gyo_z=proco(i+18)*256+proco(i+19);
          if gyo_z>=32768
                ygyoz=-bitcmp(gyo_z-1,16);
                else ygyoz=gyo_z;
          end
        

         set(handles.edit25,'String',datestr(now)); 

         j=j+1;
         if j==100
             j=1;
         end
       end
      end
end



